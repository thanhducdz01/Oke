    private void btkfindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btkfindActionPerformed

        SimpleDateFormat date = new SimpleDateFormat("dd/MM/yyyy",Locale.getDefault());
            int i = 1;
            Connection conf = db.getCon();
            try{
                String ss = "{call kfind (?)}";
                CallableStatement psk = conf.prepareCall(ss);
                psk.setString(1, date.format(dcngay.getDate()));
                psk.execute();
                ResultSet rsk = psk.getResultSet();
                model.setRowCount(0);
                while(rsk.next()){
                    Vector to = new Vector();
                    to.add(i++);
                    to.add(rsk.getString("maKH"));
                    to.add(rsk.getString("tenKH"));
                    to.add(rsk.getString("soluong"));
                    to.add(rsk.getString("DVT"));
                    to.add(rsk.getString("dongia"));
                    to.add(rsk.getString("ngaynhap"));
                    model.addRow(to);
                }
                jTable1.setModel(model);
            }catch(Exception e){JOptionPane.showMessageDialog(null, e);}
        
    }//GEN-LAST:event_btkfindActionPerformed

    private void btaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btaddActionPerformed
    
        int line = jTable1.getRowCount();
        try{
        }catch(Exception e){JOptionPane.showMessageDialog(null, "Ngày rỗng"); return;}
        if(txtiddonhang.getText().equals("") || txttendonhang.getText().equals("") || txtDVT.getText().equals("") || txtdongiak.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Có Hàng để trống");
        }else{
            try{
                while(true){
                    for(int i = 0; i< line; i++){
                        String s = (String) jTable1.getValueAt(i, 1);
                        if(txtiddonhang.getText().trim().equals(s)){
                            JOptionPane.showMessageDialog(null, "ID Trùng");
                            return;
                        }
                    }
                    if(!txtiddonhang.getText().trim().matches("KH[0-9]{4}")){
                        JOptionPane.showMessageDialog(null, "ID: KH + 4 chữ só ");
                        return;
                    }
                    int dv = Integer.parseInt(txtsoluong1.getText());
                    if(dv<0){
                        JOptionPane.showMessageDialog(null, "số lượng phải lớn hơn 1");
                        return;
                    }
                    if(Integer.parseInt(txtdongiak.getText()) < 0){
                        JOptionPane.showMessageDialog(null, "Đơn giá phải lớn 0");
                        return;
                    }else break;
                }
                SimpleDateFormat date1 = new SimpleDateFormat("dd/MM/yyyy",Locale.getDefault());
                try{
                Connection connk = db.getCon();
                String sqll = "insert into Kho values(?,?,?,?,?,?)";
                PreparedStatement psnk = connk.prepareStatement(sqll);
                psnk.setString(1, txtiddonhang.getText());
                psnk.setString(2, txttendonhang.getText());
                psnk.setInt(3, Integer.parseInt(txtsoluong1.getText()));
                psnk.setString(4, txtDVT.getText());
                psnk.setInt(5, Integer.parseInt(txtdongiak.getText()));
                psnk.setString(6, date1.format(dcngay.getDate()));
                psnk.executeUpdate();
                model.setRowCount(0);
                LoadKho();
                JOptionPane.showMessageDialog(null, "Thêm thành công");
                }catch(Exception e ){JOptionPane.showMessageDialog(null, "Lỗi");}
            }catch(Exception e){JOptionPane.showMessageDialog(null, e);}
        }
    }//GEN-LAST:event_btaddActionPerformed

