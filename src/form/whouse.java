    public void LoadKho(){
        int i = 1;
    try{
        String sql = "select * from Kho";
        ps = con.prepareStatement(sql);
        rs = ps.executeQuery();
        while(rs.next()){
            Vector row = new Vector();
            row.add(i++);
            row.add(rs.getString("maKH"));
            row.add(rs.getString("tenKH"));
            row.add(rs.getString("soluong"));
            row.add(rs.getString("DVT"));
            row.add(rs.getString("dongia"));
            row.add(rs.getString("ngaynhap"));
            model.addRow(row);
        }
        jTable1.setModel(model);
        jTable1.setDefaultEditor(Object.class, null);
    }catch(Exception e){JOptionPane.showMessageDialog(null, "Lỗi");}
    
}

